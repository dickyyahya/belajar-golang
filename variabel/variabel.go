package main

import "fmt"

func main() {
	// variabel itu ibarat gelas dan nilai sebagai isinya atau sama seperti air
	var nama string = "yahya"
	var umur int = 20

	city := "Brebes"
	year := 2026
	// sedangkan konstanta itu seperti variabel tapi nilainya tidak bisa diubah contoh pi jumlah hari
	const pi = 3.14
	const week = 7

	fmt.Println("Nama : ", nama)
	fmt.Println("Umur : ", umur)
	fmt.Println("Kota : ", city)
	fmt.Println("Tahun : ", year)
	fmt.Println("Pi :", pi)
	fmt.Println("Week :", week)

}
