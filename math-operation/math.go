package main

import "fmt"

func main() {
	a := 10
	b := 3

	fmt.Println("a+b=", a+b)   //penjumlahan
	fmt.Println("a-b=", a-b)   //pengurangan
	fmt.Println("a*b=", a*b)   //perkalian
	fmt.Println("a/b=", a/b)   //pembagian
	fmt.Println("a % b=", a%b) //modulus

	// dengan float
	x := 7.0
	y := 2.0

	fmt.Println("x/y=", x/y) //penjumlahan

}
